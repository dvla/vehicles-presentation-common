  def emailAddress: Constraint[String] = Constraint[String]("constraint.email") {
    e =>
      if (!(EmailMinLength to EmailMaxLength contains e.length)) Invalid(ValidationError("error.email"))
      else if (ptr.matcher(e).matches()) // temporarily commented out to allow us the test email DLQ: && !e.contains(" "))
        if (emailStyleValid(e)) Valid else Invalid(ValidationError("error.email"))
      else Invalid(ValidationError("error.email"))
  }

  def emailStyleValid (email: String): Boolean =
    if (email.split("@")(0).length > EmailUsernameMaxLength || (email.split("@")(0) contains InvalidUsernameChar)) false
    else if (!domainStyleValid(email.split("@")(1))) false
    else true

  def domainStyleValid(emailDomain: String): Boolean =
    if (emailDomain.contains(".")) domainSectionValid(emailDomain.split("\\."))
    else if (emailDomain.length > EmailDomainSectionMaxLength) false
    else true

  @tailrec
  def domainSectionValid(emailDomain: Seq[String]): Boolean =
    if (emailDomain.head.isEmpty) true
    else if (emailDomain.head.length > EmailDomainSectionMaxLength
      || (emailDomain.head contains InvalidDomainContentChar)
      || emailDomain.head.take(1) == InvalidDomainStartEndChar
      || emailDomain.head.takeRight(1) == InvalidDomainStartEndChar) false
    else if (emailDomain.tail.isEmpty) true
    else domainSectionValid(emailDomain.tail)
